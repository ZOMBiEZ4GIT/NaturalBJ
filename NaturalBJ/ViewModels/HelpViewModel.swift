    // â”‚                                                                       â”‚
    // â”‚ Execute search and update filtered topics.                           â”‚
    // â”‚ Called automatically after debounce delay.                           â”‚
    // â””â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”˜

    private func performSearch(query: String) {
        let trimmedQuery = query.trimmingCharacters(in: .whitespacesAndNewlines)

        print("ğŸ” Performing search: \"\(trimmedQuery)\"")
        isSearching = true

        if trimmedQuery.isEmpty {
            // Empty search - show all topics (or filtered by category)
            updateFilteredTopics()
        } else {
            // Execute search
            let results = helpManager.searchHelp(query: trimmedQuery)

            // Apply category filter if selected
            if let category = selectedCategory {
                filteredTopics = results.filter { $0.category == category }
            } else {
                filteredTopics = results
            }
        }

        isSearching = false
    }

    // â”Œâ”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”
    // â”‚ ğŸ—‘ï¸ CLEAR SEARCH                                                       â”‚
    // â”‚                                                                       â”‚
    // â”‚ Reset search query and show all topics.                              â”‚
    // â””â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”˜

    func clearSearch() {
